package graphic_interface;

public class Theme {

	// VARIABLES DE CLASSE //
	
	private String nom;
	private int id;
	
	// CONSTRUCTEURS //
	
	public Theme(String nom, int id)
	{
		
	}
	
	// GETTERS //

	public String getNom() {
		return nom;
	}

	public int getId() {
		return id;
	}
	
	// SETTERS //
	
	public void setNom(String nom) {
		this.nom = nom;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	// METHODES //
}
